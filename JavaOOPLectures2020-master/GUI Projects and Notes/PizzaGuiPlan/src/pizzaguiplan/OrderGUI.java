public void clearToppings() {
        this.excheeseCheck.setSelected(false);
        this.grPepCheck.setSelected(false);
        this.hamCheck.setSelected(false);
        this.mushroomCheck.setSelected(false);
        this.pepperoniCheck.setSelected(false);
        this.sausageCheck.setSelected(false);
    }

    private void prebuiltComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prebuiltComboActionPerformed
        // TODO add your handling code here:
        int choice = this.prebuiltCombo.getSelectedIndex();
        clearToppings();
        switch (choice) {
            case 1:
                //meat lovers   
                this.hamCheck.setSelected(true);
                this.pepperoniCheck.setSelected(true);
                this.sausageCheck.setSelected(true);
                break;
            case 2:
                //veggie lovers
                this.grPepCheck.setSelected(true);
                this.mushroomCheck.setSelected(true);
                break;
            case 3:
                //everything         
                this.excheeseCheck.setSelected(true);
                this.grPepCheck.setSelected(true);
                this.hamCheck.setSelected(true);
                this.mushroomCheck.setSelected(true);
                this.pepperoniCheck.setSelected(true);
                this.sausageCheck.setSelected(true);
                break;
            case 0:
            default:
            //deselect everything chosen
        }
        priceThePizza();
    }//GEN-LAST:event_prebuiltComboActionPerformed

