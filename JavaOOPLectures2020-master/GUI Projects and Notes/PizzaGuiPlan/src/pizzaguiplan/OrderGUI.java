    private void pizzaListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_pizzaListValueChanged
        // TODO add your handling code here:
        selectedPizza = pizzaList.getSelectedValue();
        pizzaNum = pizzaList.getSelectedIndex();

        //set the options on the screen
        if (selectedPizza.getSize().startsWith("s")) {
            this.smallRadio.setSelected(true);
        } else if (selectedPizza.getSize().startsWith("m")) {
            this.mediumRadio.setSelected(true);
        } else if (selectedPizza.getSize().startsWith("l")) {
            this.largeRadio.setSelected(true);
        }

        //repeat for sauce
        this.excheeseCheck.setSelected(selectedPizza.isExcheese());
        this.grPepCheck.setSelected(selectedPizza.isGreenpepper());
        this.hamCheck.setSelected(selectedPizza.isHam());
        this.mushroomCheck.setSelected(selectedPizza.isMushroom());
        this.pepperoniCheck.setSelected(selectedPizza.isPepperoni());
        this.sausageCheck.setSelected(selectedPizza.isSausage());
        priceThePizza();
        this.updateButton.setVisible(true);
        this.removeButton.setVisible(true);
        
    }//GEN-LAST:event_pizzaListValueChanged

