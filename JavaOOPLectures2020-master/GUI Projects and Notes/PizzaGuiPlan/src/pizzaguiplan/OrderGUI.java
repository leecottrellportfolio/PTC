    public void priceThePizza() {
        currentPizza = new Pizza(size, sauce, this.excheeseCheck.isSelected(),
                this.grPepCheck.isSelected(), this.hamCheck.isSelected(),
                this.mushroomCheck.isSelected(), this.pepperoniCheck.isSelected(),
                this.sausageCheck.isSelected());
        this.priceLabel.setText("$" + currentPizza.getPrice());
    }
    
    public void calcTotalOrder(){
        totalCost = 0;
        for(int p=0; p < pizzaListModel.getSize(); p++){
            totalCost += pizzaListModel.elementAt(p).getPrice();
        }
        
        this.totalPriceLabel.setText("$" + totalCost);
    }
